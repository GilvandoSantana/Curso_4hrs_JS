//Operadores de igualdade

//Igualdade restrita

console.log(1 === 1); //true //Números iguais
console.log(2 === 1); //false //Números diferentes
console.log('1' === 1); //false //String + Numer

//Igualdade solta

console.log(1 == 1); //true //Números iguais
console.log('1' == 1); //true //String + Numer (Ele não compara o objeto, só o valor)
console.log('2' == 1); //false //Números diferentes
